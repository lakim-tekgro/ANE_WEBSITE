              >
                Key Information
              </h4>
              <p>
                Rental accommodation is highly sought after and requires prior
                planning for international students who choose to study in
                Australia. Costs depend on the city you live in and the type of
                accommodation you choose. When choosing a place to live, you
                should consider:
              </p>
              <ul>
                <li>How far will you have to travel to the campus?</li>
                <li>Is it easily accessible by public transport?</li>
                <li>
                  Do you have access to shopping centres, hospitals, and other
                  facilities?
                </li>
                <li>Is the place furnished or unfurnished?</li>
                <li>
                  How many others will you be sharing with and what are they
                  like?
                </li>
                <li>
                  How much is the total cost? Does this cost include expenses
                  such as bond and utility fees?
                </li>
              </ul>
              <p>When applying for accommodation, here are some tips:</p>
              <ul>
                <li>
                  Do not rely on photos on a website. Make sure you visit in
                  person
                </li>
                <li>
                  Avoid paying a deposit on a place you have never seen and
                  always see lease agreement or contract first
                </li>
                <li>
                  Never give out personal details to someone you have never met
                  in exchange for more information about a property
                </li>
                <li>Is the place furnished or unfurnished?</li>
                <li>Be aware of your rights as a tenant</li>
                <li>Be aware of different types of accommodation</li>
              </ul>

              <p>When applying for accommodation, here are some tips:</p>
              <h4
