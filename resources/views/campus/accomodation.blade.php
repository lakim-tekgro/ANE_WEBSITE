              >
                Types of Accomodation
              </h4>
              <p>
                Once you have received confirmation of your studies at Torrens
                University Australia, you should look for accommodation that
                suits your needs and budget. Popular accommodation options for
                international students in Australia include:
              </p>
              <ul>
                <li>Homestay</li>
                <li>
                  Residential colleges or purpose-built student accommodation
                </li>
                <li>Private rental or share house</li>
                <li>
                  Youth hostels and other short-stay or budget accommodation
                </li>
              </ul>
              <p>
